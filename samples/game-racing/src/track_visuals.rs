// ============================================================================
// TrackVisuals — computed geometry ready for rendering
// ============================================================================

pub struct TrackVisuals {
    /// Resampled right edge for quad-strip surface rendering
    pub surface_r: Vec<Vec2>,
    /// Resampled left edge for quad-strip surface rendering
    pub surface_l: Vec<Vec2>,
    /// Original right outline (for edge lines)
    pub outline_r: Vec<Vec2>,
    /// Original left outline (for edge lines)
    pub outline_l: Vec<Vec2>,
    /// Red kerb polygons (main + sub alternating)
    pub kerb_red: Vec<Vec<Vec2>>,
    /// White kerb polygons (sub-polys)
    pub kerb_white: Vec<Vec<Vec2>>,
    /// Kerb border lines (thin white lines)
    pub kerb_borders: Vec<Vec<Vec2>>,
    /// Runoff/gravel polygons
    pub runoffs: Vec<Vec<Vec2>>,
    /// Start/finish line endpoints
    pub start_line: (Vec2, Vec2),
}

impl TrackVisuals {
    pub fn new() -> Self {
        let outline_r = convert_points(OUTLINE_R);
        let outline_l = convert_points(OUTLINE_L);

        // Resample both outlines to the same number of points for quad-strip pairing
        let num_samples = 200;
        let surface_r = resample_closed(&outline_r, num_samples);
        let surface_l = resample_closed(&outline_l, num_samples);

        // Start line
        let start_p1 = to_rengine(START_LINE[0].0, START_LINE[0].1);
        let start_p2 = to_rengine(START_LINE[1].0, START_LINE[1].1);

        // Kerbs
        let mut kerb_red: Vec<Vec<Vec2>> = Vec::new();
        let mut kerb_white: Vec<Vec<Vec2>> = Vec::new();
        let mut kerb_borders: Vec<Vec<Vec2>> = Vec::new();

        for kerb in KERBS {
            // Main polygon (always red)
            kerb_red.push(transform_polygon(kerb.main_poly, kerb.position, kerb.rotation));

            // Sub-polygons
            for sub in kerb.subs {
                // Child vertices are in the child's local space. The child has a position
                // offset relative to the parent kerb. We combine: parent will apply its
                // position + rotation on top.
                let child_verts: Vec<(f32, f32)> = sub
                    .vertices
                    .iter()
                    .map(|&(vx, vy)| (sub.position.0 + vx, sub.position.1 + vy))
                    .collect();
                let poly = transform_polygon(&child_verts, kerb.position, kerb.rotation);
                if sub.is_white {
                    kerb_white.push(poly);
                } else {
                    kerb_red.push(poly);
                }
            }

            // Border line (Line2D associated with kerb)
            let line_verts: Vec<(f32, f32)> = kerb
                .border_pts
                .iter()
                .map(|&(vx, vy)| (kerb.border_pos.0 + vx, kerb.border_pos.1 + vy))
                .collect();
            kerb_borders.push(transform_polygon(&line_verts, kerb.position, kerb.rotation));
        }

        // Runoff areas
        let runoffs: Vec<Vec<Vec2>> = RUNOFFS
            .iter()
            .map(|r| {
                r.vertices
                    .iter()
                    .map(|&(vx, vy)| to_rengine(r.position.0 + vx, r.position.1 + vy))
                    .collect()
            })
            .collect();

        Self {
            surface_r,
            surface_l,
            outline_r,
            outline_l,
            kerb_red,
            kerb_white,
            kerb_borders,
            runoffs,
            start_line: (start_p1, start_p2),
        }
    }
}
