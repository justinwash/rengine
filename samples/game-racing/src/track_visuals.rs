use rengine::Vec2;

/// Master scale factor for the entire track world (visuals + racing line).
/// Increase to make the track larger relative to car sprites.
pub const TRACK_SCALE: f32 = 2.0;

// ============================================================================
// Coordinate conversion
// ============================================================================
//
// The racing line (Path2D) is a child of Track with:
//   position = (-9.10267, -11.4433), scale = (0.666667, 0.666667)
//
// Our game world is in RacingLine-local coords with Y negated for rengine Y-up.
// To convert Track-local coords to rengine:
//   rengine_x = (track_x + 9.10267) * 1.5 * TRACK_SCALE
//   rengine_y = -(track_y + 11.4433) * 1.5 * TRACK_SCALE

fn to_rengine(track_x: f32, track_y: f32) -> Vec2 {
    let s = 1.5 * TRACK_SCALE;
    let x = (track_x + 9.10267) * s;
    let y = (track_y + 11.4433) * s;
    Vec2::new(x, -y)
}

pub(crate) fn convert_points(pts: &[(f32, f32)]) -> Vec<Vec2> {
    pts.iter().map(|&(x, y)| to_rengine(x, y)).collect()
}

/// Convert polygon vertices from a node's local space through its position+rotation
/// into Track-local space, then to rengine space.
fn transform_polygon(
    vertices: &[(f32, f32)],
    position: (f32, f32),
    rotation: f32,
) -> Vec<Vec2> {
    let (sin_r, cos_r) = rotation.sin_cos();
    vertices
        .iter()
        .map(|&(vx, vy)| {
            let tx = position.0 + vx * cos_r - vy * sin_r;
            let ty = position.1 + vx * sin_r + vy * cos_r;
            to_rengine(tx, ty)
        })
        .collect()
}

// ============================================================================
// Resampling for quad-strip pairing
// ============================================================================

fn resample_closed(points: &[Vec2], num_samples: usize) -> Vec<Vec2> {
    let n = points.len();
    if n < 2 || num_samples == 0 {
        return points.to_vec();
    }
    // Compute cumulative arc lengths (including closing segment)
    let mut lengths = Vec::with_capacity(n + 1);
    lengths.push(0.0_f32);
    for i in 1..=n {
        let prev = points[i - 1];
        let curr = points[i % n];
        lengths.push(lengths[i - 1] + prev.distance(curr));
    }
    let total = *lengths.last().unwrap();

    let mut result = Vec::with_capacity(num_samples);
    for s in 0..num_samples {
        let target = total * s as f32 / num_samples as f32;
        // Binary search for the right segment
        let mut seg = 0;
        for j in 0..lengths.len() - 1 {
            if lengths[j + 1] >= target {
                seg = j;
                break;
            }
        }
        let seg_start = lengths[seg];
        let seg_end = lengths[seg + 1];
        let t = if seg_end > seg_start {
            (target - seg_start) / (seg_end - seg_start)
        } else {
            0.0
        };
        let p0 = points[seg % n];
        let p1 = points[(seg + 1) % n];
        result.push(p0 + (p1 - p0) * t);
    }
    result
}

